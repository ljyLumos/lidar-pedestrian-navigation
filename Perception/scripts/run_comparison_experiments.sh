#!/bin/bash
#
# 伪标签检测器对比实验
# 按顺序训练: 1. Fused  2. Mask R-CNN  3. YOLOv8
#
# 使用方法:
#   chmod +x run_comparison_experiments.sh
#   ./run_comparison_experiments.sh
#

set -e  # 出错时停止

echo "============================================================"
echo "伪标签检测器对比实验"
echo "============================================================"
echo ""
echo "实验配置:"
echo "  1. Fused (Mask R-CNN + YOLOv8 融合)"
echo "  2. Mask R-CNN (原始基线)"
echo "  3. YOLOv8 (SOTA 检测器)"
echo ""
echo "每个实验训练 20 epochs"
echo "============================================================"
echo ""

# 进入项目目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "${SCRIPT_DIR}/.."

# ============================================================
# 实验 1: Fused 伪标签
# ============================================================
echo ""
echo "============================================================"
echo "[1/3] 开始训练: Fused 伪标签"
