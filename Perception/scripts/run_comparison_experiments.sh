echo "[1/3] Fused 训练完成: $(date)"
echo ""

# ============================================================
# 实验 2: Mask R-CNN 伪标签
# ============================================================
echo ""
echo "============================================================"
echo "[2/3] 开始训练: Mask R-CNN 伪标签"
echo "============================================================"
echo "开始时间: $(date)"
echo ""

python bin/train.py --cfg cfgs/dr_spaam_jrdb_maskrcnn_pseudo.yaml

echo ""
echo "[2/3] Mask R-CNN 训练完成: $(date)"
echo ""

# ============================================================
# 实验 3: YOLOv8 伪标签
# ============================================================
echo ""
echo "============================================================"
echo "[3/3] 开始训练: YOLOv8 伪标签"
echo "============================================================"
echo "开始时间: $(date)"
echo ""

python bin/train.py --cfg cfgs/dr_spaam_jrdb_yolov8_pseudo.yaml

echo ""
echo "[3/3] YOLOv8 训练完成: $(date)"
echo ""

# ============================================================
# 完成
# ============================================================
echo ""
echo "============================================================"
echo "所有实验完成!"
echo "============================================================"
echo ""
echo "训练结果保存在 ./logs/ 目录:"
echo "  - dr_spaam_jrdb_fused_pseudo/"
echo "  - dr_spaam_jrdb_maskrcnn_pseudo/"
echo "  - dr_spaam_jrdb_yolov8_pseudo/"
echo ""
echo "接下来可以运行评估脚本对比三个模型的性能"
